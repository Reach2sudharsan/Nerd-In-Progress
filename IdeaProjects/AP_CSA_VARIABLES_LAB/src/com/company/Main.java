/*
The program below prints values from 9 different data types, and they are int,
String, double, float, boolean, byte, long, short, and char.
 */
package com.company;

public class Main {

    public static void main(String[] args) {
        int number = 1;
        String str = "Word";
        double decimalNumber1 = 5.0d;
        float float1 = 10f;
        boolean trueOrFalse = true;
        byte byte1 = 120;
        long long1 = 1000000000L;
        short short1 = 10000;
        char char1 = 'A';

        System.out.println("int number: " + number);
        System.out.println("String str: " + str);
        System.out.println("double decimalNumber1: " + decimalNumber1);
        System.out.println("float float1: " + float1);
        System.out.println("boolean trueOrFalse: " + trueOrFalse);
        System.out.println("byte byte1: " + byte1);
        System.out.println("long long1: " + long1);
        System.out.println("short short1: " + short1);
        System.out.println("char char1: " + char1);



	// write your code here
    }
}
